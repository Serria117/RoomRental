    private void LoadData() {
        servicelist = serviceController.displayService();
        if (servicelist != null) {
            defaultTableModel.setRowCount(0);
            servicelist.stream().forEach(s -> {
                defaultTableModel.addRow(new Object[]{
                    //defaultTableModel.getRowCount() + 1,
                    s.getId(),
                    s.getServiceName(),
                    s.getPrice(),
                    s.getUnit()
                });
            });
        }
    }

