    public void search(String svName) {
        defaultTableModel.setRowCount(0);
        servicelist = serviceController.SearchService(svName);
        servicelist.stream().forEach(s -> {
            defaultTableModel.addRow(new Object[]{
                s.getId(),
                s.getServiceName(),
                s.getPrice(),
                s.getUnit()
            });
        });

    }

