    static List<GuestDTO> gList = new ArrayList<>();

    public void loadGuestDTO() {
        guestTB_Model.setRowCount(0);
        gList = guestController.getAll();
        gList.stream().forEach(g -> {
            guestTB_Model.addRow(new Object[]{
                g.getFullName(),
                g.getCitizenId(),
                g.getPhone(),
                g.getDateOfBirth(),
                g.getStatus()
            });
        });

    }

